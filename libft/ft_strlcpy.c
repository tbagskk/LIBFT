#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t		i;
	size_t		size;

	i = 0;
	size = ft_strlen(src);
	if (dstsize == 0)
		return (size);
	while (i < (dstsize -1) && src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = 0;
	while (src[i])
		i++;
	return (i);
}
